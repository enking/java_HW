package com.controlFlow2.normal;

public class Application2 {
    public static void main(String[] args) {
        for (int i = 'a'; i<='z'; i++ ){
            System.out.print((char)i);
        }
    }// 출력시 char로 형 변환 해줘서 a부터 z까지 출력하게 만들어 주는 것이 포인트
}


